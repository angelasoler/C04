#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

int	main()
{
	char source[100] = "Me imprima caracter por caracter, obgd.\n";
	ft_putstr(source);
}